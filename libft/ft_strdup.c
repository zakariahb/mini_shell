#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	char	*p;
	size_t	len;

	i = 0;
	len = ft_strlen(s1);
	p = malloc(len + 1);
	if (!p)
		return (NULL);
	while (s1[i])
	{
		p[i] = s1[i];
		i++;
	}
	p[i] = '\0';
	return (p);
}
