#include "libft.h"

int	ft_atoi(const char *str)
{
	int		i;
	long	res;
	int		sign;

	i = 0;
	res = 0;
	sign = 1;
	while (str[i] == 32 || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign *= (-1);
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		if (res > (LONG_MAX / 10) || (res == (LONG_MAX / 10) && str[i] > '7'))
			return (-1 * (sign != -1));
		res = res * 10 + (str[i] - '0');
		i++;
	}
	return (res * sign);
}
